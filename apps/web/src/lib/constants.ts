// Helper function to get state by FIPS code
export function getStateByFips(fips: string): StateData | undefined {
  return US_STATES[fips]
}

// Helper function to get state by code
export function getStateByCode(code: string): StateData | undefined {
  return Object.values(US_STATES).find((state) => state.code === code)
}

// Helper function to get population density
export function getPopulationDensity(fips: string): number | undefined {
  const state = US_STATES[fips]
  return state ? state.population / state.area_sq_mi : undefined
}
